export function AuthPanel() {
  const { isAuthenticated, user, login, register, logout, loading, error } = useAuthContext();
  const [showAuthModal, setShowAuthModal] = useState(false);
  const [authMode, setAuthMode] = useState<'login' | 'register'>('login');
  
  // États du formulaire
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [fullName, setFullName] = useState('');
  const [showPassword, setShowPassword] = useState(false);

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    const success = await login(email, password);
    if (success) {
      closeModal();
    }
  };

  const handleRegister = async (e: React.FormEvent) => {
    e.preventDefault();
    const success = await register(email, password, fullName);
    if (success) {
      closeModal();
    }
  };

  // Réinitialiser les champs à la fermeture
  const closeModal = () => {
    setShowAuthModal(false);
    setEmail('');
    setPassword('');
    setFullName('');
    setAuthMode('login');
  };

  const handleGoogleLogin = () => {
    // Logique future pour Google Auth
    console.log("Connexion Google demandée");
  };

  const toggleAuthMode = () => {
    setAuthMode(authMode === 'login' ? 'register' : 'login');
    // Petit reset des erreurs si nécessaire
  };

  return (
    <>
      {/* --- Bouton d'accès dans la Navbar --- */}
      <div className="flex items-center ml-auto">
        {isAuthenticated ? (
          <div className="flex items-center gap-4 bg-white/10 backdrop-blur-md px-4 py-2 rounded-full border border-white/20 shadow-sm">
            <div className="flex items-center gap-3">
              <div className="w-8 h-8 bg-gradient-to-tr from-blue-600 to-indigo-600 rounded-full flex items-center justify-center text-white font-bold text-sm shadow-md">
                {user.name ? user.name.charAt(0).toUpperCase() : 'U'}
              </div>
              <div className="hidden md:block text-right mr-2">
                <div className="text-gray-800 font-semibold text-sm leading-tight">{user.name || 'Utilisateur'}</div>
                <div className="text-gray-500 text-xs">{user.email}</div>
              </div>
            </div>
            <div className="h-6 w-px bg-gray-300 mx-1"></div>
            <button
              onClick={logout}
              className="text-gray-500 hover:text-red-600 transition-colors p-1 rounded-full hover:bg-red-50"
              title="Se déconnecter"
            >
              <LogOut size={18} />
            </button>
          </div>
        ) : (
          <button
            onClick={() => setShowAuthModal(true)}
            className="group px-5 py-2.5 bg-gray-900 text-white rounded-xl hover:bg-gray-800 transition-all duration-200 shadow-lg hover:shadow-xl flex items-center gap-2 font-medium text-sm"
          >
            <LogIn size={16} className="text-gray-300 group-hover:text-white transition-colors" />
            Espace Membre
          </button>
        )}
      </div>

      {/* --- Modale Authentification Premium --- */}
      {showAuthModal && (
        <div className="fixed inset-0 z-50 flex items-center justify-center p-4">
          {/* Backdrop avec flou */}
          <div 
            className="absolute inset-0 bg-gray-900/60 backdrop-blur-sm transition-opacity"
            onClick={closeModal}
          ></div>

          {/* Carte Principale */}
          <div className="relative bg-white w-full max-w-md rounded-2xl shadow-2xl overflow-hidden transform transition-all animate-in fade-in zoom-in-95 duration-200">
            
            {/* Bouton Fermer */}
            <button
              onClick={closeModal}
              className="absolute top-4 right-4 p-2 text-gray-400 hover:text-gray-600 hover:bg-gray-100 rounded-full transition-colors z-10"
            >
              <X size={20} />
            </button>

            <div className="p-6 sm:p-8">
              
              {/* En-tête avec Branding */}
              <div className="text-center mb-8">
                <div className="inline-flex items-center justify-center w-12 h-12 rounded-xl bg-blue-50 text-blue-600 mb-4">
                  {/* Logo Placeholder ou Icône */}
                  <div className="font-bold text-xl">N</div>
                </div>
                <h2 className="text-2xl font-bold text-gray-900 tracking-tight mb-2">
                  Bienvenue sur NexusBi
                </h2>
                <p className="text-gray-600">
                  {authMode === 'login'
                    ? 'Connectez-vous pour accéder à votre tableau de bord.'
                    : 'Créez votre compte pour commencer l\'aventure.'}
                </p>
              </div>

              {/* Connexion Sociale (Google) */}
              <button
                onClick={handleGoogleLogin}
                className="w-full flex items-center justify-center gap-3 bg-white border border-gray-300 hover:bg-gray-50 text-gray-800 font-medium py-3 px-4 rounded-xl transition-all duration-200 mb-6"
              >
                <GoogleIcon />
                <span>Continuer avec Google</span>
              </button>

              <div className="relative flex items-center gap-4 py-2 mb-6">
                <div className="flex-grow border-t border-gray-200"></div>
                <span className="flex-shrink-0 text-gray-400 text-sm uppercase tracking-wider font-medium">Ou via email</span>
                <div className="flex-grow border-t border-gray-200"></div>
              </div>

              {/* Affichage des erreurs */}
              {error && (
                <div className="mb-6 p-4 bg-red-50 border-l-4 border-red-500 rounded-r-md flex items-start gap-3">
                  <div className="text-red-500 mt-0.5"><User size={16} /></div> {/* Icone d'alerte si dispo, sinon User temporaire */}
                  <p className="text-red-700 text-sm font-medium">{error}</p>
                </div>
              )}

              {/* Formulaire */}
              <form onSubmit={authMode === 'login' ? handleLogin : handleRegister} className="space-y-5">
                
                {authMode === 'register' && (
                  <div className="space-y-1.5">
                    <label className="text-sm font-semibold text-gray-800 ml-1">Nom complet</label>
                    <div className="relative group">
                      <div className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400 group-focus-within:text-blue-600 transition-colors">
                        <User size={18} />
                      </div>
                      <input
                        type="text"
                        value={fullName}
                        onChange={(e) => setFullName(e.target.value)}
                        placeholder="Jean Dupont"
                        className="w-full pl-10 pr-4 py-3 bg-gray-50 border border-gray-200 rounded-xl focus:bg-white focus:outline-none focus:ring-2 focus:ring-blue-500/20 focus:border-blue-500 transition-all text-gray-800 placeholder-gray-400"
                        required
                      />
                    </div>
                  </div>
                )}

                <div className="space-y-1.5">
                  <label className="text-sm font-semibold text-gray-800 ml-1">Email professionnel</label>
                  <div className="relative group">
                    <div className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400 group-focus-within:text-blue-600 transition-colors">
                      <Mail size={18} />
                    </div>
                    <input
                      type="email"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      placeholder="nom@entreprise.com"
                      className="w-full pl-10 pr-4 py-3 bg-gray-50 border border-gray-200 rounded-xl focus:bg-white focus:outline-none focus:ring-2 focus:ring-blue-500/20 focus:border-blue-500 transition-all text-gray-800 placeholder-gray-400"
                      required
                    />
                  </div>
                </div>

                <div className="space-y-1.5">
                  <div className="flex justify-between items-center ml-1">
                    <label className="text-sm font-semibold text-gray-800">Mot de passe</label>
                    {authMode === 'login' && (
                      <a href="#" className="text-xs text-blue-600 hover:text-blue-700 font-medium">
                        Oublié ?
                      </a>
                    )}
                  </div>
                  <div className="relative group">
                    <div className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400 group-focus-within:text-blue-600 transition-colors">
                      <Lock size={18} />
                    </div>
                    <input
                      type={showPassword ? 'text' : 'password'}
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      placeholder="••••••••"
                      className="w-full pl-10 pr-12 py-3 bg-gray-50 border border-gray-200 rounded-xl focus:bg-white focus:outline-none focus:ring-2 focus:ring-blue-500/20 focus:border-blue-500 transition-all text-gray-800 placeholder-gray-400"
                      required
                      minLength={6}
                    />
                    <button
                      type="button"
                      onClick={() => setShowPassword(!showPassword)}
                      className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 hover:text-gray-600 p-1 rounded-md transition-colors"
                    >
                      {showPassword ? <EyeOff size={18} /> : <Eye size={18} />}
                    </button>
                  </div>
                </div>

                <button
                  type="submit"
                  disabled={loading}
                  className="w-full py-3 px-4 bg-gradient-to-r from-blue-600 to-indigo-700 hover:from-blue-700 hover:to-indigo-800 text-white font-semibold rounded-xl shadow-lg hover:shadow-blue-500/30 transition-all duration-200 flex items-center justify-center gap-2 mt-4 border border-blue-600/20 hover:border-blue-700/30"
                >
                  {loading ? (
                    <div className="flex items-center gap-2">
                      <div className="w-5 h-5 border-2 border-white/30 border-t-white rounded-full animate-spin"></div>
                      <span>Traitement...</span>
                    </div>
                  ) : authMode === 'login' ? (
                    <>
                      <span>Se connecter</span>
                      <ArrowRight size={18} />
                    </>
                  ) : (
                    <>
                      <span>Créer mon compte</span>
                      <UserPlus size={18} />
                    </>
                  )}
                </button>
              </form>
            </div>

            {/* Pied de carte (Footer) */}
            <div className="bg-gray-50 px-6 py-4 text-center border-t border-gray-200">
              <p className="text-gray-700 text-sm">
                {authMode === 'login' ? "Vous n'avez pas encore de compte ?" : "Vous avez déjà un compte ?"}
                <button
                  onClick={toggleAuthMode}
                  className="ml-2 text-blue-600 font-semibold hover:underline focus:outline-none"
                >
                  {authMode === 'login' ? "S'inscrire gratuitement" : "Se connecter"}
                </button>
              </p>
            </div>
          </div>
        </div>
      )}
    </>
  );
}